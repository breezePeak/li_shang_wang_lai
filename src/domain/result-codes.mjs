export function success(data = null, message = '') {
  return { ok: true, code: RESULT_CODES.SUCCESS, message, data };
}

export function blocking(code, message, { recoverable = true, data = null } = {}) {
  return { ok: false, code, message, recoverable, data };
}

export function fatal(code, message, data = null) {
  return { ok: false, code, message, recoverable: false, data };
}
